class Solution {
public:

    int ok(ll y, vi& v, ll m){
        ll c = 0;

        for(auto x: v){
            c+=(ll)sqrt(y/x);
        }

        // cout << c << " " << y << endl;

        return c>=m;
    }

    long long repairCars(vector<int>& v, int m) {
        ll n = v.size();
        ll k = (ll)*min_element(all(v))*m*m;
        ll j = k;

        while(k){
            while(j-k>0 and ok(j-k, v, m)) j-=k;
            k/=2;
        }

        return j;
    }
};